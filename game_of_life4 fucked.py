# Define the size of the grid
ROWS = 30
COLS = 50
CELL_SIZE = 10

# Function to create a random matrix
def random_matrix():
    return [[random.randint(0, 1) for _ in range(COLS)] for _ in range(ROWS)]

# Function to initialize the canvas with a blank state
def initialize_canvas():
    return [[0 for _ in range(COLS)] for _ in range(ROWS)]

# Function to draw the current state of the matrix on the canvas
def draw_canvas(matrix):
    canvas.delete("all")
    for row in range(ROWS):
        for col in range(COLS):
            if matrix[row][col]:
                canvas.create_rectangle(col * CELL_SIZE, row * CELL_SIZE,
                                        (col + 1) * CELL_SIZE, (row + 1) * CELL_SIZE,
                                        fill="white")
            else:
                canvas.create_rectangle(col * CELL_SIZE, row * CELL_SIZE,
                                        (col + 1) * CELL_SIZE, (row + 1) * CELL_SIZE,
                                        fill="black")

# Function to handle mouse clicks on the canvas
def on_canvas_click(event):
    col = event.x // CELL_SIZE
    row = event.y // CELL_SIZE
    # Toggle the cell state (alive <-> dead)
    current_matrix[row][col] = 1 - current_matrix[row][col]
    draw_canvas(current_matrix)

# Function to start the game loop
def start_game():
    global running
    running = True
    while running:
        update_game()
        draw_canvas(current_matrix)
        root.update()
        time.sleep(speed_scale.get())

# Function to update the game state (one generation)
def update_game():
    new_matrix = [[0 for _ in range(COLS)] for _ in range(ROWS)]
    for row in range(ROWS):
        for col in range(COLS):
            new_matrix[row][col] = get_next_state(row, col)
    current_matrix[:] = new_matrix[:]

# Function to calculate the next state of a cell

def get_next_state(matrix):
    new_matrix = [[0 for _ in range(COLS)] for _ in range(ROWS)]
    for row in range(ROWS):
        for col in range(COLS):
            alive_neighbors = count_alive_neighbors(matrix, row, col)
            if matrix[row][col] == 1:
                if alive_neighbors == 2 or alive_neighbors == 3:
                    new_matrix[row][col] = 1
            else:
                if alive_neighbors == 3:
                    new_matrix[row][col] = 1
    return new_matrix
# Function to stop the game loop
def stop_game():
    global running
    running = False

# Create the main window
root = tk.Tk()
root.title("Game of Life")

# Create a canvas to display the game grid
canvas = tk.Canvas(root, width=COLS * CELL_SIZE, height=ROWS * CELL_SIZE, bg="black")
canvas.bind("<Button-1>", on_canvas_click)
canvas.pack()

# Create buttons to control the game
start_button = tk.Button(root, text="Start", command=start_game)
start_button.pack(side=tk.LEFT)
stop_button = tk.Button(root, text="Stop", command=stop_game)
stop_button.pack(side=tk.LEFT)
clear_button = tk.Button(root, text="Clear", command=lambda: initialize_canvas())
clear_button.pack(side=tk.LEFT)

# Speed scale for controlling the speed of the game
speed_scale = tk.Scale(root, from_=0.1, to=1.0, resolution=0.1, label="Speed")
speed_scale.pack(side=tk.LEFT)

# Initialize the current matrix and the running flag
current_matrix = initialize_canvas()
running = False

# Start the Tkinter main loop
root.mainloop()
